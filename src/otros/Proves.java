    private void tablaComunidadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaComunidadesMouseClicked
        String id = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 0).toString();
        tfId.setText(id);
        String nombre = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 1).toString();
        tfNombre.setText(nombre);
        String direccion = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 2).toString();
        tfDireccion.setText(direccion);
        String cp = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 3).toString();
        tfCp.setText(cp);
        String poblacion = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 4).toString();
        tfPoblacion.setText(poblacion);
        String provincia = tablaComunidades.getValueAt(tablaComunidades.getSelectedRow(), 5).toString();
        tfProvincia.setText(provincia);
        lComunidad.setText(nombre);
        muestraTablaViviendas(Integer.parseInt(id));
        muestraTablaFacturas(Integer.parseInt(id));
        clearTable((DefaultTableModel)tablaPropietarios.getModel());
    }//GEN-LAST:event_tablaComunidadesMouseClicked
    
